// Función para obtener imagen aleatoria por categoría
export function getRandomImage(category) {
  const images = categoryImages[category] || categoryImages.naturaleza
  return images[Math.floor(Math.random() * images.length)]
}

// Función para obtener imagen por región
export function getRegionImage(region, type = 'isla') {
  return ecuadorImages[region]?.[type] || ecuadorImages.sierra.quito
}

